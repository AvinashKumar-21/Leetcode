class Solution
{
    public:
    Node *compute(Node *head)
    {
        stack<Node*>s;
        s.push(head);
        head=head->next;
        while(head)
        {
            Node*a=head->next;
            head->next=NULL;
            while(!s.empty() && s.top()->data < head->data)
            s.pop();
            s.push(head);
            head=a;
        }
        Node*temp=NULL;
        while(!s.empty())
        {
            if(temp==NULL)
            temp=s.top();
            else
            {
                s.top()->next=temp;
                temp=s.top();
            }
            s.pop();
        }
        return temp;
    }
    
};
   


